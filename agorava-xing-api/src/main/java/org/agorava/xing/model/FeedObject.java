package org.agorava.xing.model;

import java.util.Date;

import org.agorava.api.function.Nameable;
import org.agorava.xing.function.HasTypeAndId;

/**
 * Model class representing a feed object
 * @author Werner Keil
 */
public class FeedObject extends HasTypeAndId implements Nameable {

	private User creator;
	private Date createdAt;
	private String title;
	private String url;
	private String description;
	private String image;
	private String bookmarkType;
	private String displayName;
	private String permalink;
	private String name;
	private String photo;
	private String content;

	public User getCreator() {
		return creator;
	}

	public void setCreator(User creator) {
		this.creator = creator;
	}

	public Date getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(Date createdAt) {
		this.createdAt = createdAt;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getBookmarkType() {
		return bookmarkType;
	}

	public void setBookmarkType(String bookmarkType) {
		this.bookmarkType = bookmarkType;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getPermalink() {
		return permalink;
	}

	public void setPermalink(String permalink) {
		this.permalink = permalink;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
