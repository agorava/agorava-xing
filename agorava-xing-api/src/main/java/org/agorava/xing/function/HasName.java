package org.agorava.xing.function;

import org.agorava.api.function.Nameable;

/**
 * Generic abstract item with a name.
 *
 * @author Werner Keil
 */
@SuppressWarnings("serial")
public abstract class HasName implements Nameable {

	protected String name;

    /**
     * Gets the value of the name property.
     * 
     * @return
     *     possible object is
     *     {@link String }
     *     
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the value of the name property.
     * 
     * @param value
     *     allowed object is
     *     {@link String }
     *     
     */
    public void setName(String value) {
        this.name = value;
    }
}
